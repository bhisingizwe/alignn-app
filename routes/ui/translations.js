function getAlignnLanguage() {
  return localStorage.getItem("alignnLanguage") || "en";
}

function getAlignnText(key) {
  const lang = getAlignnLanguage();
  return translations[lang]?.[key] || translations.en[key] || key;
}

function translateCommonLayout() {
  const tagline = document.querySelector(".tagline");
  if (tagline) tagline.textContent = getAlignnText("tagline");

  const backDashboard = document.querySelector(".back-dashboard-link");
  if (backDashboard) backDashboard.textContent = getAlignnText("backDashboard");

  const sideLinks = document.querySelectorAll(".dashboard-icon-link span:last-child");
  if (sideLinks.length >= 7) {
    sideLinks[0].textContent = getAlignnText("home");
    sideLinks[1].textContent = getAlignnText("mood");
    sideLinks[2].textContent = getAlignnText("checkIn");
    sideLinks[3].textContent = getAlignnText("journal");
    sideLinks[4].textContent = getAlignnText("reflection");
    sideLinks[5].textContent = getAlignnText("profileTitle");
    sideLinks[6].textContent = getAlignnText("history");
  }

  const logoutBtn = document.getElementById("logoutBtnSide");
  if (logoutBtn) logoutBtn.textContent = getAlignnText("logout");

  const footerLinks = document.querySelectorAll(".footer-link");
  if (footerLinks.length >= 3) {
    footerLinks[0].textContent = getAlignnText("about");
    footerLinks[1].textContent = getAlignnText("contact");
    footerLinks[2].textContent = getAlignnText("crisis");
  }
}

function applyAlignnTranslations() {
  translateCommonLayout();
}

window.addEventListener("DOMContentLoaded", applyAlignnTranslations);